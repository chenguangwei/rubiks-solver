const dictionaries: Record<Language, Messages> = { en, zh, ja, ko, fr }

type I18nValue = string | number

type I18nContextValue = {
  language: Language
  setLanguage: (language: Language) => void
  t: (key: string, values?: Record<string, I18nValue>) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

const defaultI18n: I18nContextValue = {
  language: 'en',
  setLanguage: () => {},
  t(key, values) {
    return interpolate(en[key] ?? key, values)
  },
}

function normalizeLanguage(input: string | null | undefined): Language | null {
  if (!input) return null
  const code = input.toLowerCase().split('-')[0]
  return LANGUAGE_OPTIONS.some((option) => option.code === code) ? (code as Language) : null
}

function readInitialLanguage(): Language {
  if (typeof window === 'undefined') return 'en'
  try {
    const isManualOverride = window.localStorage.getItem(LS_LANGUAGE_SOURCE) === 'manual'
    const stored = normalizeLanguage(window.localStorage.getItem(LS_LANGUAGE))
    if (isManualOverride && stored) return stored
  } catch {
    // Ignore storage errors and fall through to browser language.
  }
  for (const candidate of window.navigator.languages ?? [window.navigator.language]) {
    const normalized = normalizeLanguage(candidate)
    if (normalized) return normalized
  }
  return 'en'
}

function interpolate(message: string, values?: Record<string, I18nValue>) {
  if (!values) return message
  return message.replace(/\{(\w+)\}/g, (_, key: string) =>
    Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : `{${key}}`,
  )
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(readInitialLanguage)

  useEffect(() => {
    document.documentElement.lang = HTML_LANG[language]
  }, [language])

  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage)
    try {
      window.localStorage.setItem(LS_LANGUAGE, nextLanguage)
      window.localStorage.setItem(LS_LANGUAGE_SOURCE, 'manual')
    } catch {
      // Language selection is still usable without persistence.
    }
  }, [])

  const value = useMemo<I18nContextValue>(() => {
    return {
      language,
      setLanguage,
      t(key, values) {
        const message = dictionaries[language][key] ?? en[key] ?? key
        return interpolate(message, values)
      },
    }
  }, [language, setLanguage])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  return context ?? defaultI18n
}
